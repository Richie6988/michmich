// ============================================================
// AVATAR COLOR (matches the in-app palette)
// ============================================================
const AVATAR_PALETTE = ['#2563EB', '#F97316', '#10B981', '#8B5CF6', '#EF4444', '#EC4899', '#06B6D4', '#F59E0B'];
function colorForUser(userId?: string | null, fallback?: string | null): string {
  const seed = userId || fallback || 'unknown';
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) - hash) + seed.charCodeAt(i);
    hash |= 0;
  }
  return AVATAR_PALETTE[Math.abs(hash) % AVATAR_PALETTE.length];
}

// ============================================================
// ICS CALENDAR — RFC 5545
// ============================================================

function pad2(n: number) { return n < 10 ? `0${n}` : `${n}`; }

function toIcsDateTime(d: Date): string {
  return `${d.getUTCFullYear()}${pad2(d.getUTCMonth() + 1)}${pad2(d.getUTCDate())}T${pad2(d.getUTCHours())}${pad2(d.getUTCMinutes())}${pad2(d.getUTCSeconds())}Z`;
}

function toIcsDate(d: Date): string {
  return `${d.getUTCFullYear()}${pad2(d.getUTCMonth() + 1)}${pad2(d.getUTCDate())}`;
}

function escIcs(s: string): string {
  return (s || '').replace(/\\/g, '\\\\').replace(/;/g, '\\;').replace(/,/g, '\\,').replace(/\n/g, '\\n').replace(/\r/g, '');
}

export function buildIcs(ctx: ExportContext): string {
  const { trip } = ctx;
  const now = toIcsDateTime(new Date());
  const isTrip = trip.mode === 'trip';
  const start = trip.scheduledAt ? new Date(trip.scheduledAt) : null;
  const end = trip.endDate ? new Date(trip.endDate) : null;

  const lines: string[] = [
    'BEGIN:VCALENDAR',
    'VERSION:2.0',
    'PRODID:-//Barry//Trip Export//EN',
    'CALSCALE:GREGORIAN',
    'METHOD:PUBLISH',
  ];

  if (start) {
    lines.push('BEGIN:VEVENT');
    lines.push(`UID:${trip.id}@barry.app`);
    lines.push(`DTSTAMP:${now}`);

    if (isTrip && end) {
      lines.push(`DTSTART;VALUE=DATE:${toIcsDate(start)}`);
      const exclusiveEnd = new Date(end);
      exclusiveEnd.setUTCDate(exclusiveEnd.getUTCDate() + 1);
      lines.push(`DTEND;VALUE=DATE:${toIcsDate(exclusiveEnd)}`);
    } else {
      lines.push(`DTSTART;VALUE=DATE:${toIcsDate(start)}`);
      lines.push(`DTEND;VALUE=DATE:${toIcsDate(start)}`);
    }

    lines.push(`SUMMARY:${escIcs(`Barry: ${trip.name}`)}`);

    const description = [
      `Organized by ${trip.organizer?.firstName || ''} ${trip.organizer?.lastName || ''}`.trim(),
      `${trip.participants.length} participant${trip.participants.length === 1 ? '' : 's'}`,
      `Mode: ${isTrip ? 'Multi-day trip' : 'Wanderlust outing'}`,
      ctx.totalCost ? `Total cost: ${ctx.totalCost.toFixed(0)} EUR` : '',
      '',
      'Generated by Barry, your group meetup planner.',
    ].filter(Boolean).join('\\n');
    lines.push(`DESCRIPTION:${description}`);

    if (trip.selectedVenueId) {
      lines.push(`LOCATION:${escIcs(trip.selectedVenueId)}`);
    }
    lines.push('END:VEVENT');
  }

  lines.push('END:VCALENDAR');
  return lines.join('\r\n');
}

export function downloadIcs(ctx: ExportContext) {
  const ics = buildIcs(ctx);
  const blob = new Blob([ics], { type: 'text/calendar;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `barry-${ctx.trip.name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}.ics`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

