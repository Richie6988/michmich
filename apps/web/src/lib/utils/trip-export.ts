export function downloadPdf(ctx: ExportContext) {
  const html = buildPdfHtml(ctx);
  const w = window.open('', '_blank');
  if (!w) {
    alert('Please allow pop-ups to export your trip recap.');
    return;
  }
  w.document.write(html);
  w.document.close();
}
